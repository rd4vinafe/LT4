/**
  * @brief This function handles TIM2 global interrupt.
  */
void TIM2_IRQHandler(void)
{
  /* USER CODE BEGIN TIM2_IRQn 0 */
  uint8_t dim1 = 0, dim2 = 0;
  dim1 = (duty[0] * 200) / 255;
  dim2 = (duty[1] * 200) / 255;
  if (__HAL_TIM_GET_FLAG(&htim2, TIM_FLAG_UPDATE) != RESET)
  {
      if (__HAL_TIM_GET_IT_SOURCE(&htim2, TIM_IT_UPDATE) != RESET)
      {
          __HAL_TIM_CLEAR_IT(&htim2, TIM_IT_UPDATE);  // clear flag thủ công

          if (buff_zero[0])
          {
              tick++;

              // TRIAC1 (PB12)
              if (tick >= dim1 && tick < (dim1 + 3))
                  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_12, GPIO_PIN_RESET); // kích
              else if (tick >= (dim1 + 3))
                  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_12, GPIO_PIN_SET);   // tắt

              // TRIAC2 (PB13)
              if (tick >= dim2 && tick < (dim2 + 3))
                  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_13, GPIO_PIN_RESET);
              else if (tick >= (dim2 + 3))
                  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_13, GPIO_PIN_SET);

              // Hết 1 chu kỳ 20ms
              if (tick >= 200)
              {
                  tick = 0;
                  buff_zero[0] = 0;
              }
          }
      }
  }
  /* USER CODE END TIM2_IRQn 0 */
  
  /* USER CODE BEGIN TIM2_IRQn 1 */
  
  /* USER CODE END TIM2_IRQn 1 */
}

